倒车出/入库：
        1.上车系安全带

        2.调整座椅，方向盘上部与手腕相碰，头部与车顶两个手指

        3.打火

        4.观察仪表盘是否有门没关好，手刹没松等指示灯

        5.继4 后，松手刹，门关好
        
        6.挂1挡

        7.脚踩离合与刹车

        8.松离合，松刹车

        9.车头抵线，右转弯或者左转弯打死最右边，或者最左边，（转方向）

        10.车子摆正后，迅速回盘，回正

        11.肩膀与左边/右边线持平

        12.停-踩刹车，离合踩到底

        13.挂倒挡

        14.车耳朵柄，遇矩形线后边重合，迅速方向盘右打死/左打死

        15.车身与后部位，30cm左右，回拨180°方向盘

        16.当车身与线平行，迅速拨正
        
        17.倒至车耳朵柄与线重合

